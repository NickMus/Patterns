package Facade;

public class Car {
    public void turnOnIgnition() {
        System.out.println("Ignition turn on");
    }
    public void turnOnFuelPump() {
        System.out.println("fuel pump turn on");
    }
    public void spinStarter() {
        System.out.println("starter are spinning");
    }
    public void makeSparkOnPlugs() {
        System.out.println("plugs are sparkling");
    }
    public void engineWorking() {
        System.out.println("engine is working");
    }
    public void makeOffSparkOnPlugs() {
        System.out.println("plugs are not sparkling");
    }
    public void engineStopping() {
        System.out.println("engine has stopped");
    }
    public void turnOffIgnition() {
        System.out.println("Ignition turn off");
    }
    public void turnOffFuelPump() {
        System.out.println("fuel pump turn off");
    }
}
